pessoa = {'nome': 'Vitor', 'idade': 19, 'cidade': 'Recife'}  # Criação de um dicionario
print(pessoa['cidade'])  # usar a key  para acessar o elemento

pessoa['país'] = 'Brasil'  # Adiciona mais um elemento no dicionario
print(pessoa)

print(pessoa.keys())  # Para acessar todas as chaves do dicionário

print(pessoa.values())  # Para acessar todos os valores do dicionário

